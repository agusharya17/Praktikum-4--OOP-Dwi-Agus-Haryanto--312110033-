package bangundatar;

public class Persegi extends BangunDatar{
    // Membuat variabel yang dibutuhkan
    
    int sisi;
    
    public Persegi(int sisi){
        this.sisi = sisi;
    }
    
    @Override
    public float luas(){
        return this.sisi = this.sisi;
    }
    
    @Override
    public float keliling(){
        return this.sisi = this.sisi * 4;
    }
}